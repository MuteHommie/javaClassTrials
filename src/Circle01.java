public class Circle01 {
    private double radius;

    public Circle01() {
        this.radius = radius;
    }

    public Circle01(double radius) {
        this.radius = radius;
    }

    public double getRadius() {
        return radius;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }

    public double getArea() {
        return Math.PI * radius * radius;
    }
}

